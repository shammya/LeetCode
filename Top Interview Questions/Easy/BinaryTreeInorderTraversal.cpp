/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */

// solution idea
// 1.recursive inorder traversal
// 2.pass the vector as a reference
// 3.the vector stores the inorder traveresed nodes

void traverse(vector<int> &order, TreeNode *node)
{
    if (node == NULL)
        return;
    traverse(order, node->left);
    order.push_back(node->val);
    traverse(order, node->right);
}

vector<int> inorderTraversal(TreeNode *root)
{
    vector<int> order;
    traverse(order, root);
    return order;
}

